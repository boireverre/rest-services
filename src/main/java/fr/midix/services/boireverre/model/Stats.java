package fr.midix.services.boireverre.model;

public class Stats {

	private int usersNumber;
	private int rdvNumber;
	private int rdvActiveNumber;
	
	public int getUsersNumber() {
		return usersNumber;
	}
	public void setUsersNumber(int usersNumber) {
		this.usersNumber = usersNumber;
	}
	public int getRdvNumber() {
		return rdvNumber;
	}
	public void setRdvNumber(int rdvNumber) {
		this.rdvNumber = rdvNumber;
	}
	public int getRdvActiveNumber() {
		return rdvActiveNumber;
	}
	public void setRdvActiveNumber(int rdvActiveNumber) {
		this.rdvActiveNumber = rdvActiveNumber;
	}
	
	
}
